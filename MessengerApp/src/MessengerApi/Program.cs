using MessengerDomain.Config;
using MessengerService;

namespace MessengerApi
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            builder.Services.Configure<SMTPConfig>(builder.Configuration.GetSection("SMTPConfig"));
            builder.Services.Configure<NotificationsTimeConfig>(builder.Configuration.GetSection("NotificationsTimeConfig"));
            builder.Services.AddScoped<MessengerService.IEmailService, MessengerService.EmailService>();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}